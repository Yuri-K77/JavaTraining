/* Свойство массива length считает длину массива автоматически */

package arrays;

public class ArrayLength {
    public static void main(String[] args) {
        int [] array = {5, 17, 350, 2, 456, 20, 19};
        int arraySize = array.length;
        System.out.println("Длина массива " + arraySize + " элементов");
    }
}